package openlabBook;

public class SwitchCase {

	public static void main(String[] args) {
		int tech=4;
		switch(tech) {
		case 1:{
			System.out.println("java");
			break;
		}
		case 2:{
			System.out.println("Python");
			break;
		}
		case 3:{
			System.out.println("DBMS");
			break;
		}
		default:{
			System.out.println("Not Listed.....");
		}
		}

	}

}
